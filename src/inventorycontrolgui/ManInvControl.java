    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int index = jComboBox1.getSelectedIndex();
        if(qtyLi[index] < (Integer) jSpinner2.getValue()){
            JOptionPane.showMessageDialog(null,"Cannot remove more than in stock");
        }else{
        String USERNAME = "Blanc";
        String PASSWORD = "shiro";
        String url =
        "jdbc:mysql://localhost:3306/Inventory";
        try{
            Connection connection = DriverManager.getConnection(url, USERNAME, PASSWORD);
            PreparedStatement inst = connection.prepareStatement(" insert into removes values(now(), ?,?,?);");
            inst.setString(1, store);
            inst.setString(2, (String) jComboBox1.getSelectedItem());
            inst.setInt(3, (Integer) jSpinner2.getValue());
            inst.executeUpdate();
            qtyLi[index] -= (Integer) jSpinner2.getValue();
            jTextField1.setText(String.valueOf(qtyLi[index]));
        }catch(Exception e){
            System.err.print(e);
        }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

