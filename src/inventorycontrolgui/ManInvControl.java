    public ManInvControl(String strID, String empID){
        initComponents();
        this.store=strID;
        this.id=empID;
        
        
        String USERNAME = "Blanc";
        String PASSWORD = "shiro";
        String url =
            "jdbc:mysql://localhost:3306/Inventory";
        try{
            Connection connection = DriverManager.getConnection(url, USERNAME, PASSWORD);
            Statement st = (Statement) connection.createStatement();
            String prepare2 = "select*from inventory where storeID = '" + store + "';";
            ResultSet rs = st.executeQuery(prepare2);
            
            int x = 0;
                    
            while(rs.next()){
                x++;
            }
            this.itemLi = new String[x];
            this.thresLi = new int[x];
            this.qtyLi = new int[x];
            
            rs.close();
            
            rs = st.executeQuery("select*from inventory where storeID = '" + store + "';");            
            while(rs.next()){
                String ite = rs.getString("itemID");
                itemLi[rs.getRow()-1] = ite;
                int thres = rs.getInt("threshold");
                thresLi[rs.getRow()-1] = thres;
                int qty = rs.getInt("quantity");
                qtyLi[rs.getRow()-1] = qty;
            }
            
            jComboBox1.setModel(new javax.swing.DefaultComboBoxModel<>(itemLi));
            int index = jComboBox1.getSelectedIndex();
            jTextField1.setText(String.valueOf(qtyLi[index]));
            jSpinner1.setValue((Integer) thresLi[index]);
        }catch(SQLException e){
            System.err.println(e);
        }
        
    }

