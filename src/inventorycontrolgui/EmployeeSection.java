    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            if((Integer) jSpinner1.getValue()<=0){
                JOptionPane.showMessageDialog(null, "quantity cannot be less than 0");
            }else if((Integer) jSpinner1.getValue() > Integer.parseInt(jTextField3.getText())){
                JOptionPane.showMessageDialog(null, "quantity more than there is in stock");
            }else{
                String USERNAME = "Blanc";
                String PASSWORD = "shiro";
                String url =
                    "jdbc:mysql://localhost:3306/Inventory";
                try{
                    Connection connection = DriverManager.getConnection(url, USERNAME, PASSWORD);
                    Statement st = (Statement) connection.createStatement();
                    PreparedStatement inst = connection.prepareStatement("Insert into Transactions values(now(), ?,?,?,?)");
                    inst.setString(1, this.store);
                    inst.setString(2, (String) jComboBox1.getSelectedItem());
                    inst.setInt(3, (Integer) jSpinner1.getValue());
                    inst.setString(4, this.id);
                    inst.executeUpdate();
                    
                    int index = jComboBox1.getSelectedIndex();
                    this.qty[index] -= (Integer) jSpinner1.getValue();
                    jTextField3.setText(String.valueOf(this.qty[index]));
                    
                }catch(Exception e){
                    System.err.print(e);
                }
            }
        }catch(Exception e){
            System.err.print(e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

