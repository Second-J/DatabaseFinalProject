    public EmployeeSection() {
        initComponents();
        this.store = EmployeeLogin.store;
        this.id = EmployeeLogin.id;
        
        String USERNAME = "Blanc";
        String PASSWORD = "shiro";
        String url =
            "jdbc:mysql://localhost:3306/Inventory";
        try{
            Connection connection = DriverManager.getConnection(url, USERNAME, PASSWORD);
            Statement st = (Statement) connection.createStatement();
            String prepare = "select*from inventory left join items on inventory.itemID = items.itemID where storeID = '"+ store +"';";
            ResultSet rs = st.executeQuery(prepare);
            
            int x = 0;
                    
            while(rs.next()){
                x++;
            }
            String [] itemID = new String[x];
            this.itemName = new String[x];
            this.price = new double[x];
            this.qty = new int[x];

            ResultSet qs = st.executeQuery("select*from inventory left join items on inventory.itemID = items.itemID where storeID = '" + store + "';");            
            while(qs.next()){
                String iI = qs.getString("itemID");
                itemID[qs.getRow()-1] = iI;
                String iN = qs.getString("itemName");
                itemName[qs.getRow()-1] = iN;
                double iP = qs.getDouble("price");
                price[qs.getRow()-1] = iP;
                int iQ = qs.getInt("quantity");
                qty[qs.getRow()-1] = iQ;
            }
            jComboBox1.setModel(new javax.swing.DefaultComboBoxModel<>(itemID));
            int index = jComboBox1.getSelectedIndex();
            jTextField1.setText(this.itemName[index]);
            jTextField2.setText(String.valueOf(this.price[index]));
            jTextField3.setText(String.valueOf(this.qty[index]));
        }catch(SQLException e){
            System.err.println(e);
        }
    }

